package fundamentos;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		//Operadores Relacionais == != >= > <= <
		int num1 = 97;
		int num2 = 'a';

		System.out.println(num1 == num2);//num1 é igual a num2?
		System.out.println(3 != 2); //!= significa diferente, ou seja 3 é diferente de 2? true!
		System.out.println(3 >= 3);
		System.out.println(3 > 4);
		
	}
}
